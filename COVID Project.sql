USE CovidProject

Select * 
from CovidDeaths1
WHERE CONTINENT IS NOT NULL

--Study Total Cases vs Total Deaths

Select total_cases, total_deaths, (total_deaths/total_cases)*100 as Death_Percentage
from CovidDeaths1
WHERE Location='India'
order by 1,3 ASC

--Looking at Total Cases vs Population

Select Location, Date, total_cases, population, (total_cases/population)*100 as Infection_Rate
from CovidDeaths1
WHERE Location='India'
order by 1,2

-- Countries with highest infection rate compared to population

Select Location, population, MAX(total_cases) AS HighestInfectionCount, MAX((total_cases/population))*100 AS PopInfected
from CovidDeaths1
GROUP BY Location, population
ORDER BY PopInfected DESC

-- Showing countries with highest death count per population
Select Location, population, MAX(total_deaths) AS TotalDeathCount, MAX((total_deaths/population))*100 AS PopDead
from CovidDeaths1
WHERE CONTINENT IS NOT NULL
GROUP BY Location, population
ORDER BY TotalDeathCount DESC

-- Breaking things down by continent instead of location

SELECT continent, MAX(Total_deaths) as TotalDeathCount
FROM CovidDeaths1
WHERE CONTINENT IS not NULL
GROUP BY continent
ORDER BY TotalDeathCount DESC

-- GLOBAL NUMBERS

Select date, max(total_cases) as total_cases, max(total_deaths) as total_deaths
from CovidDeaths1
GROUP BY date
 
